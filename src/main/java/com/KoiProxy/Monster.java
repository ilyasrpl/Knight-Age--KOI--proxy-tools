package com.KoiProxy;

public class Monster {
    public short id;
    public String name;
    public short level;
    public int hp;
    public byte idk;

    public Monster(short id, String name, short level, int hp, byte idk) {
        this.id = id;
        this.name = name;
        this.level = level;
        this.hp = hp;
        this.idk = idk;
    }
}
